/// <summary>
/// 풀링 시스템에서 사용되는 인터페이스입니다.
/// 이 인터페이스는 풀에서 관리되는 오브젝트가 구현해야 하는 메서드를 정의합니다.
/// </summary>
public interface IPoolable
{
    /// <summary>
    /// 오브젝트가 풀에서 꺼내질 때 호출됩니다.
    /// 이 메서드는 오브젝트가 활성화될 때 필요한 초기화 작업을 수행합니다.
    /// </summary>
    void OnSpawn();

    /// <summary>
    /// 오브젝트가 풀로 돌아갈 때 호출됩니다.
    /// 이 메서드는 오브젝트가 비활성화될 때 필요한 정리 작업을 수행합니다.
    /// </summary>
    void OnDespawn();
}
